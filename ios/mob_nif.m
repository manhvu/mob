// ── NIF: add_child/2 ─────────────────────────────────────────────────────────

static ERL_NIF_TERM nif_add_child(ErlNifEnv* env, int argc, const ERL_NIF_TERM argv[]) {
    ViewRes *parent_res, *child_res;
    if (!enif_get_resource(env, argv[0], view_res_type, (void**)&parent_res) ||
        !enif_get_resource(env, argv[1], view_res_type, (void**)&child_res))
        return enif_make_badarg(env);

    MobNode* parent = (__bridge MobNode*)parent_res->node;
    MobNode* child  = (__bridge MobNode*)child_res->node;
    [parent.children addObject:child];
    return enif_make_atom(env, "ok");
}

