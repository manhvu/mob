// mob_nif.m — Mob UI NIF for iOS (SwiftUI).
// Module name: mob_nif
//
// NIFs build a MobNode tree, then set_root/1 pushes it to MobViewModel where
// SwiftUI re-renders reactively. No UIKit calls here; no dispatch_sync needed.
//
// Thread safety: MobNode mutations happen on the BEAM scheduler thread.
// set_root dispatches to the main thread via MobViewModel.setRoot(), which
// is @Published and fires the SwiftUI diff on the correct thread.
//
// Memory: MobNode* objects are stored as void* using CFBridgingRetain (+1
// manual retain outside ARC). The NIF resource destructor balances with
// CFRelease.

#import <Foundation/Foundation.h>
#include <string.h>
#include "erl_nif.h"
#import "MobNode.h"
// Generated by swiftc -emit-objc-header; exposes MobViewModel and MobUIFactory.
