// ── NIF: on_tap/2 ─────────────────────────────────────────────────────────────

static ERL_NIF_TERM nif_on_tap(ErlNifEnv* env, int argc, const ERL_NIF_TERM argv[]) {
    ViewRes* res;
    ErlNifPid pid;
    if (!enif_get_resource(env, argv[0], view_res_type, (void**)&res) ||
        !enif_get_local_pid(env, argv[1], &pid))
        return enif_make_badarg(env);

    res->tap_pid     = pid;
    res->has_tap_pid = 1;

    // Capture the pid by value in the block. Inside the block we make a mutable
    // copy so we can take its address for enif_send (which takes ErlNifPid*).
    ErlNifPid captured_pid = pid;
    MobNode* node = (__bridge MobNode*)res->node;
    node.onTap = ^{
        ErlNifPid local_pid = captured_pid;
        ErlNifEnv* msg_env = enif_alloc_env();
        ERL_NIF_TERM msg = enif_make_tuple2(msg_env,
            enif_make_atom(msg_env, "tap"),
            enif_make_atom(msg_env, "ok"));
        enif_send(NULL, &local_pid, msg_env, msg);
        enif_free_env(msg_env);
    };

    return enif_make_atom(env, "ok");
}

// ── NIF: set_root/1 ──────────────────────────────────────────────────────────

static ERL_NIF_TERM nif_set_root(ErlNifEnv* env, int argc, const ERL_NIF_TERM argv[]) {
    ViewRes* res;
    if (!enif_get_resource(env, argv[0], view_res_type, (void**)&res))
        return enif_make_badarg(env);

    MobNode* node = (__bridge MobNode*)res->node;
    [[MobViewModel shared] setRoot:node];
    LOGI(@"set_root: pushed node type=%ld", (long)node.nodeType);
    return enif_make_atom(env, "ok");
}

